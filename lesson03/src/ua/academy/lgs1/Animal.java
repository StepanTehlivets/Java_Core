package ua.academy.lgs1;

public class Animal {
	private String name;
	private int speed;
	private int age;
	
	Animal(){
		this.name="Леопард";
		this.speed=20;
		this.age=7;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSpeed() {
		return speed;
	}

	public void setSpeed(int speed) {
		this.speed = speed;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}


	
	
}
